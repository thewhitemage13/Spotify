package org.spotify.enums;

/**
 * Enum representing the various genres of music.
 * <p>
 * The {@code Genre} enum defines a set of music genres that can be associated with songs, performers, or collections.
 * It includes common genres such as Classical, Jazz, Rock, Pop, and more.
 * </p>
 *
 * @author Mukhammed Lolo
 * @version 1.0
 */
public enum Genre {

    /**
     * Classical music genre.
     */
    Classical,

    /**
     * Jazz music genre.
     */
    Jazz,

    /**
     * Hip-hop or Rap music genre.
     */
    HipHopRap,

    /**
     * Electronic music genre.
     */
    Electronic,

    /**
     * Rock music genre.
     */
    Rock,

    /**
     * Disco music genre.
     */
    Disco,

    /**
     * Fusion music genre.
     */
    Fusion,

    /**
     * Pop music genre.
     */
    PopMusic,

    /**
     * Country music genre.
     */
    Country,

    /**
     * Blues music genre.
     */
    Blues,

    /**
     * Reggae music genre.
     */
    Reggae,

    /**
     * Indie music genre.
     */
    Indie
}
