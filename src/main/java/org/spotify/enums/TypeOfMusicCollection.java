package org.spotify.enums;

/**
 * Enum representing the different types of music collections.
 * <p>
 * The {@code TypeOfMusicCollection} enum defines the two primary types of music collections:
 * {@code Album} and {@code EP}. These can be used to categorize music collections based
 * on their format or length.
 * </p>
 *
 * @author Mukhammed Lolo
 * @version 1.0
 */
public enum TypeOfMusicCollection {

    /**
     * An Album, typically a full-length music collection.
     */
    Album,

    /**
     * An EP (Extended Play), typically shorter than an album, but longer than a single.
     */
    EP
}
